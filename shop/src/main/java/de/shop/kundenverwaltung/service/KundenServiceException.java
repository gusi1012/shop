package de.shop.kundenverwaltung.service;

import de.shop.util.ShopException;


public abstract class KundenServiceException extends ShopException {
	private static final long serialVersionUID = -2849585609393128387L;

	public KundenServiceException(String msg) {
		super(msg);
	}
	
	public KundenServiceException(String msg, Throwable t) {
		super(msg, t);
	}
}
